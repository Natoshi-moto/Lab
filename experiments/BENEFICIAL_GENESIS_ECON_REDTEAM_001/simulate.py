#!/usr/bin/env python3
"""Deterministic scenario runner for BGEN-ECON-REDTEAM-001.

Reads every manifest under ``scenarios/*.json``, runs it through
``model.scenario.run_scenario``, and writes one result file per scenario
under ``results/`` plus an aggregate ``results/SUMMARY.json`` and a
human-readable ``results/TABLES.md``. Stdlib only; no network access.

Usage (from repository root):

    python3 experiments/BENEFICIAL_GENESIS_ECON_REDTEAM_001/simulate.py
"""

from __future__ import annotations

import json
import pathlib
import sys

HERE = pathlib.Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))

from model.scenario import run_scenario  # noqa: E402


def load_manifests(scenarios_dir: pathlib.Path) -> list[dict]:
    manifests = []
    for path in sorted(scenarios_dir.glob("*.json")):
        with path.open() as f:
            manifests.append(json.load(f))
    return manifests


def render_tables(results: list[dict]) -> str:
    lines = [
        "# Generated concentration, welfare and attack-profitability tables",
        "",
        "Generated by `simulate.py`. Do not hand-edit; regenerate instead.",
        "",
        "## Concentration and welfare",
        "",
        "| scenario | scheme | donors | unissued | gini (owner) | HHI (owner) | top1 (owner) | top10 (owner) |",
        "|---|---|---|---|---|---|---|---|",
    ]
    for r in results:
        owner = r["concentration"]["by_beneficial_owner"]
        lines.append(
            f"| {r['scenario_id']} | {r['allocation_scheme']} | {r['donor_count']} | "
            f"{r['unissued_remainder']} | {owner['gini']} | {owner['hhi']} | "
            f"{owner['top1_share']} | {owner['top10_share']} |"
        )

    lines += ["", "## Governance concentration (where modelled)", "",
              "| scenario | max owner governance share | crosses majority | crosses blocking third |",
              "|---|---|---|---|"]
    for r in results:
        gov = r.get("governance")
        if gov is None:
            continue
        lines.append(
            f"| {r['scenario_id']} | {gov.get('max_owner_governance_share', 'n/a')} | "
            f"{gov.get('crosses_simple_majority', 'n/a')} | {gov.get('crosses_blocking_third', 'n/a')} |"
        )

    lines += ["", "## Attack-profitability matrix", "",
              "| scenario | attack class | key result |", "|---|---|---|"]
    for r in results:
        for attack, payload in r.get("attack_analysis", {}).items():
            summary = json.dumps(payload)
            if len(summary) > 180:
                summary = summary[:177] + "..."
            lines.append(f"| {r['scenario_id']} | {attack} | `{summary}` |")

    lines += ["", "## Donor economics under token-value multipliers", "",
              "| scenario | multiplier | token_value_per_unit | mean utility | profitable donors | unprofitable donors |",
              "|---|---|---|---|---|---|"]
    for r in results:
        for multiplier, econ in r.get("donor_economics", {}).items():
            lines.append(
                f"| {r['scenario_id']} | {multiplier} | {econ['token_value_per_unit']} | "
                f"{econ['mean_utility']} | {econ['profitable_donor_count']} | {econ['unprofitable_donor_count']} |"
            )

    return "\n".join(lines) + "\n"


def main() -> int:
    scenarios_dir = HERE / "scenarios"
    results_dir = HERE / "results"
    results_dir.mkdir(exist_ok=True)

    manifests = load_manifests(scenarios_dir)
    if not manifests:
        print("no scenario manifests found", file=sys.stderr)
        return 1

    results = []
    for manifest in manifests:
        result = run_scenario(manifest)
        results.append(result)
        out_path = results_dir / f"{result['scenario_id']}.json"
        with out_path.open("w") as f:
            json.dump(result, f, indent=2, sort_keys=True)
            f.write("\n")

    summary_path = results_dir / "SUMMARY.json"
    with summary_path.open("w") as f:
        json.dump(
            {"scenario_count": len(results), "scenario_ids": [r["scenario_id"] for r in results]},
            f,
            indent=2,
        )
        f.write("\n")

    tables_path = results_dir / "TABLES.md"
    tables_path.write_text(render_tables(results))

    print(f"ran {len(results)} scenarios; wrote results to {results_dir}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
